'''
1.Факториал
Написать программу, которая считает факториал произвольного числа. Не
использовать метод для расчета факториала из стандартной библитоеки.
Факториал считается как:
5! = 5 × 4 × 3 × 2 × 1 = 120
'''

print(f'{"_"*20}FIRST TASK{"_"*20}')

num_start = int(input('Введите произвольное число: '))
if num_start > 0:
    factorial = 1
    for i in range(1, num_start+1):
        factorial *= i
    print(f'Factorial: {num_start}! = {factorial}')
else:
    print('Ошибка. Факториал — функция, определённая на множестве неотрицательных целых чисел')

