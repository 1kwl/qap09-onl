#3.Быки и коровы
#В классическом варианте игра рассчитана на двух игроков. Каждый из игроков задумывает
#и записывает тайное 4-значное число с неповторяющимися цифрами. Игрок, который
#начинает игру по жребию, делает первую попытку отгадать число. Попытка — это 4-
#значное число с неповторяющимися цифрами, сообщаемое противнику. Противник
#сообщает в ответ, сколько цифр угадано без совпадения с их позициями в тайном числе
#(то есть количество коров) и сколько угадано вплоть до позиции в тайном числе (то есть
#количество быков).
#При игре против компьютера игрок вводит комбинации одну за другой, пока не отгадает
#всю последовательность.
#Ваша задача реализовать программу, против которой можно сыграть в "Быки и коровы"
#Пример
#Загадано число 3219
#>>> 2310
#Две коровы, один бык
#>>> 3219
#Вы выиграли!

print(f'{"_"*20}THIRD TASK{"_"*20}')
import random

start_random = random.sample('1234567890', 4)
start_comp = ''.join(start_random)
start_gamer = input('Отгадайте 4-значное число с неповторяющимися цифрами: ')
print(start_comp)
while start_gamer != start_comp:
    korova_number = 0
    byk_number = 0
    for korova in start_gamer:
        if korova in start_comp:
            korova_number += 1
    for byk in range(len(start_gamer)):
        if start_gamer[byk] == start_comp[byk]:
            byk_number += 1
    print(f' {str(korova_number- byk_number)} KOROVA')
    print(f' {byk_number} BYK')
    start_gamer = input(f'Не отгадал. У тебя {str(korova_number - byk_number)} коровы и {byk_number} бык. Попробуй еще : ')
else:
    print('Вы выиграли!')






